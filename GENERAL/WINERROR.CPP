Error& Error::SayAndQuit(const char *fmt, ...)
{
	static	char String [256];

	va_list	marker;

	va_start(marker, fmt);

	Master_3d.QuitGame();

	vsprintf(String, fmt, marker);
	exitmsg=String;

	MessageBox (Master_3d.winst, String, "SysError", MB_OK);
	FILE* file=fopen(errorfilename,"wt");
	if (file)
	{
		int i;
		for (i=0;i<300;i++)
			breakif(exitmsg[i]==0);
		fwrite(exitmsg,i+1,1,file);
		fwrite("\n",2,1,file);
		fclose(file);
	}
	file=fopen(buerrorfilename,"at");
	if (file)
	{
		int i;
		for (i=0;i<300;i++)
			breakif(exitmsg[i]==0);
		fwrite(exitmsg,i,1,file);
		fwrite("\n",1,1,file);
		fclose(file);
	}

	exit(0);
	return	*(Error*)NULL;

}

