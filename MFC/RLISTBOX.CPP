void CRListBox::ReplaceString(LPCTSTR text, short row, short col)
{
	RListBoxData& data = GetData(this);
	if (col < 0) col = 0;
    if (row < 0) row = 0;

    // Ensure column exists in colWidths
    while (data.colWidths.size() <= (size_t)col) {
        data.colWidths.push_back(100); // Default width
    }

	std::vector<std::string>& column = data.columns[col];
    
    // Ensure row exists
    if (row >= (int)column.size()) {
        column.resize(row + 1);
    }
    
    column[row] = text ? text : "";
    
    if (row >= data.rowCount) {
        data.rowCount = row + 1;
    }
    
    if (m_hWnd) Invalidate();
}

