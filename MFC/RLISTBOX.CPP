void CRListBox::ResizeToFit()
{
	RListBoxData& data = GetData(this);
	int totalWidth = 0;
	for (int w : data.colWidths) {
		totalWidth += w;
	}
	if (totalWidth == 0 && !data.columns.empty()) totalWidth = 100;

	int rowHeight = GetRowHeight(this);
	int totalHeight = data.rowCount * rowHeight;
	if (totalHeight == 0) totalHeight = rowHeight;

	SetWindowPos(NULL, 0, 0, totalWidth, totalHeight, SWP_NOMOVE | SWP_NOZORDER | SWP_NOACTIVATE);
}

