void CRListBox::InsertRow(long row)
{
    RListBoxData& data = GetData(this);
    if (row < 0) row = 0;
    if (row > data.rowCount) row = data.rowCount;

    for (auto& pair : data.columns) {
        std::vector<std::string>& col = pair.second;
        if (row <= (int)col.size()) {
            col.insert(col.begin() + row, "");
        } else {
            col.resize(row + 1, "");
        }
    }
    if (row <= (int)data.playerNums.size()) {
        data.playerNums.insert(data.playerNums.begin() + row, 0);
    }

    // Shift icons
    std::map<std::pair<int,int>, int> newIcons;
    for (auto& kv : data.icons) {
        int r = kv.first.first;
        int c = kv.first.second;
        if (r >= row) r++;
        newIcons[{r, c}] = kv.second;
    }
    data.icons = newIcons;
    
    data.rowCount++;
    if (m_hWnd) Invalidate();
}

void CRListBox::DeleteRow(long row)
{
    RListBoxData& data = GetData(this);
    if (row < 0 || row >= data.rowCount) return;
    
    for (auto& pair : data.columns) {
        std::vector<std::string>& col = pair.second;
        if (row < (int)col.size()) {
            col.erase(col.begin() + row);
        }
    }
    if (row < (int)data.playerNums.size()) {
        data.playerNums.erase(data.playerNums.begin() + row);
    }

    // Shift icons
    std::map<std::pair<int,int>, int> newIcons;
    for (auto& kv : data.icons) {
        int r = kv.first.first;
        int c = kv.first.second;
        if (r == row) continue;
        if (r > row) r--;
        newIcons[{r, c}] = kv.second;
    }
    data.icons = newIcons;

    data.rowCount--;
    if (m_hWnd) Invalidate();
}

void CRListBox::Shrink()
{
	RListBoxData& data = GetData(this);
	if (data.columns.empty()) {
		return;
	}

	CDC dc(this);
	CFont* pOldFont = nullptr;
	
	CWnd* pParent = GetParent();
	if (pParent) {
		CFont* pGlobalFont = (CFont*)pParent->SendMessage(WM_GETGLOBALFONT, data.fontNum);
		if (pGlobalFont) {
			pOldFont = dc.SelectObject(pGlobalFont);
		}
	}

	for (size_t col = 0; col < data.colWidths.size(); ++col) {
		int maxWidth = 0;
		if (data.columns.count(col)) {
			for (const auto& text : data.columns[col]) {
				CSize size = dc.GetTextExtent(text.c_str());
				if (size.cx > maxWidth) {
					maxWidth = size.cx;
				}
			}
		}
		data.colWidths[col] = maxWidth + 10; // Add some padding
	}

	if (pOldFont) {
		dc.SelectObject(pOldFont);
	}
}

